class RegionData {
  constructor(countryCode, name, nativeName, lat, lan, population, flag) {
    this.countryCode = countryCode;
    this.name = name;
    this.nativeName = nativeName;
    this.lat = lat;
    this.lan = lan;
    this.population = population;
    this.flag = flag;
  }
}

export default RegionData;
